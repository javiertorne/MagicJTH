import CoreGraphics

/// Estructura de datos con los valores correspondientes a medidas de márgenes, etc.
struct Measurement {

    // MARK: - Margins
    
    /// Medida que corresponde a la mitad del margen normal.
    static let halfMargin: CGFloat = 8
    
    /// Margen normal.
    static let margin: CGFloat = 16
    
    /// Medida que corresponde al doble del margen normal.
    static let doubleMargin: CGFloat = 32
    
    /// Medida que corresponde al triple del margen normal.
    static let tripleMargin: CGFloat = 48
    
    /// Medida que corresponde al cuadruple del margen normal.
    static let quadrupleMargin: CGFloat = 64

}
